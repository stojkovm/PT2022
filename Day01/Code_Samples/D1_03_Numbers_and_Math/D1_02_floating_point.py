"""
- Floating-point number or float is a number with decimal places.
- If we want to check what is the type of a value of a variable,
    we can call the built-in function type()
"""
x = type(1.0)










"""
- We can create float by calling the built-in function float()
    and passing the string representation of an integer.
"""
float("33.3")

"""
- To provide better readability for large float literals,
    we can use underscore (_) BETWEEN the digits.
"""
y = 1_000_00_0.0









"""
- We can use exponential or E-notation for really large or small numbers.
"""
z = 1e+5
