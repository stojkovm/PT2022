"""
- Python has support for complex numbers.
"""
x = 2 + 3j

# reading only real component
x.real

# reading only imaginary component
x.imag

# return the complex conjugate of the number
x.conjugate()

# you can do all arithmetic operations that work with integers and floats
x = 1 + 3j
y = 2 - 2j
z = x - y
