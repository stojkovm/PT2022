"""
- Integer is a whole number with no decimal places.
- If we want to check what is the type of a value of a variable,
    we can call the built-in function type()
"""
x = type(1)








"""
- We can create an integer by calling the built-in function int()
    and passing the string representation of an integer.
"""
y = int("33")








"""
- To provide better readability for large integer literals,
    we can use underscore (_) BETWEEN the digits.
"""
z = 1_000_00_0
